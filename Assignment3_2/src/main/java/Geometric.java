
public class Geometric {

	public double circleDiameter(int r) {
		double result = 0.0;
		result = 2 * r;
		return result;
	}
	
	public double circlePerimeter(int r) {
		double result = 0.0;
		result = (22  * 2 * r) / 7;
		return result;
	}

	public double circleArea(int r) {
		double result = 0.0;
		result = (22 * r * r)/ 7;
		return result;
	}
	
}
